#operadores logicos

valor1 = 500
valor2 =  1000
valor3 = 1250
#operador (and)

print("verificaçao do valor1:", valor1<valor2 and valor1<valor3)
print("verificacao do valor2", valor2>valor1 and valor2>valor3)
# operador (or)

print("verificacao do valor1:", valor1>valor2 or valor1>valor3)  

#operador (not)

print("verificaçao do valor1:", not valor1<valor2 or valor1<valor3)
print("verificacao do valor2", not valor2>valor1 or valor2>valor3)

