#Operadores aritmetricos (cont)

valor1 = 80
valor2 = 10

print("Exponenciacao: ", valor1**3 )
print("Divisao padrao: ", valor1 / valor2)
# duas barras deixa o valor inteiro
print("divisao sem decimais: ", valor1//valor2)
print("Modulo(resto) da divisao: ", valor2%3)

# Operadores de comparaçao (Relacionais)

print("valor1 é maior que o valor2: ", valor1>valor2)
print("Valor1 é maior ou igual ao valor2", valor1>=valor2)
print("valor1 é menor ou igual ao valor2", valor1<=valor2)
print("valor1 é igual ao valor2", valor1==valor2)
print("valor1 é diferente de valor2", valor1 != valor2)